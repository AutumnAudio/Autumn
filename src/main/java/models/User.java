package models;

public class User {

  /**
   * Username.
   */
  private String username;

  /**
   * Get name of the user.
   * @return String
   */
  public String getUsername() {
    return username;
  }

  /**
   * Set username for the user.
   * @param username String
   */
  public void setUsername(final String name) {
    this.username = name;
  }

}
